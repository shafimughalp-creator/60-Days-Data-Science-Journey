
# 2️⃣ Normal Distribution (Very Important)

# The Normal Distribution is the most important distribution in data science.

# It looks like a bell-shaped curve.

# Characteristics:

# Symmetrical

# Most values near the mean

# Fewer values at extremes

# Example:

# Human heights
# Exam scores
# Measurement errors

# Key Properties

# 1. Mean = Median = Mode

# In normal distribution:

# Mean = Median = Mode

# The center is perfectly balanced.

# 2. Bell Curve Shape
#        *
#      *   *
#    *       *
#  *           *
#*               *

# Most data is around the center.

# 3. 68–95–99.7 Rule

# This is extremely important.

# In normal distribution:

# Range	Data Coverage
# ±1 Standard Deviation	68%
# ±2 Standard Deviations	95%
# ±3 Standard Deviations	99.7%

# Example:

# Mean exam score = 70
# Std deviation = 5

# Then:

# 68% scores between 65–75

# 95% between 60–80
