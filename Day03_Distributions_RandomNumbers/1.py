

# 🚀 Day 3 — Distributions + Random Numbers (Complete Guide)

# 1️⃣ What is a Data Distribution?

# A distribution describes how data values are spread.

# It answers questions like:

# Are most values small or large?

# Are values evenly spread?

# Are there extreme values?

# Example dataset:

# [60, 65, 70, 72, 75, 80, 82]

# If you plot it, you see how values are distributed.

# Why Data Scientists Care

# Distributions help us understand:

# Customer spending patterns

# Exam score behavior

# Sales trends

#  Outliers

# Model assumptions

# Many machine learning models assume data follows certain distributions.