

# 8️⃣ Generate Normal Distribution Data

import numpy as np 

data = np.random.normal( loc=50 , scale= 10 , size=100 )

print(data)

# loc = mean 
# scale = standard deviation 
# size = number of samples 

# This simulates real-world data.

